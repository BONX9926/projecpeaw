<script>
function check_pass(el){
    if(el.attr('id') == 'password'){
        if (el.val() != '') {
            if(el.val() === $('#password-confirm').val()){
                $('#save').removeAttr('disabled');
            }else{
                $('#save').attr('disabled', 'disabled');
            }
        }
    }else{
        if (el.val() != '') {
            if(el.val() === $('#password').val()){
                $('#save').removeAttr('disabled');
            }else{
                $('#save').attr('disabled', 'disabled');
            }
        }
    }
}
</script>